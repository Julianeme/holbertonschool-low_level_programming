/**
 * op_add - adds a + b
 * @a: int
 * @b: int
 * Return: a+b.
 */
int op_add(int a, int b)
{ return (a + b); }
/**
 * op_sub - bus b from a
 * @a: int
 * @b: int
 * Return: a-b.
 */
int op_sub(int a, int b)
{ return (a - b); }
/**
 * op_mul - multiplies a times b
 * @a: int
 * @b: int
 * Return: a*b.
 */
int op_mul(int a, int b)
{ return (a * b); }
/**
 * op_div - divides a between b
 * @a: int
 * @b: int
 * Return: a/b.
 */
int op_div(int a, int b)
{ return (a / b); }
/**
 * op_mod - returns the module of the division of a between b
 * @a: int
 * @b: int
 * Return: a%b.
 */
int op_mod(int a, int b)
{ return (a % b); }
